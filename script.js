const html = document.documentElement;
const langButton = document.querySelector("[data-lang-toggle]");
const mobileButton = document.querySelector("[data-mobile-toggle]");
const navLinks = document.querySelector("[data-nav-links]");

function applyLanguage(lang) {
  const dictionary = translations[lang] || translations.en;
  html.lang = lang === "id" ? "id" : "en";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.getAttribute("data-i18n-placeholder");
    if (dictionary[key]) {
      node.setAttribute("placeholder", dictionary[key]);
    }
  });
  localStorage.setItem("bgu-language", lang);
}

const storedLanguage = localStorage.getItem("bgu-language") || "en";
applyLanguage(storedLanguage);

if (langButton) {
  langButton.addEventListener("click", () => {
    const next = html.lang === "id" ? "en" : "id";
    applyLanguage(next);
  });
}

if (mobileButton && navLinks) {
  mobileButton.addEventListener("click", () => {
    const open = navLinks.classList.toggle("open");
    mobileButton.setAttribute("aria-expanded", String(open));
  });
}

document.querySelectorAll("[data-contact-form]").forEach((form) => {
  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const data = new FormData(form);
    const message = encodeURIComponent(
      `Halo Black Garlic Unti, saya ${data.get("name") || ""}. ${data.get("message") || ""}`
    );
    window.location.href = `https://wa.me/6281392385171?text=${message}`;
  });
});
